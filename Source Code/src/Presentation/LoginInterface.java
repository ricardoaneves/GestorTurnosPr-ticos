    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        String username = UsernameTextField.getText();
        String password = PasswordField.getText();
        int auth = this.facade.login(username,password);
        switch (auth) {
            case 0:
                LoginAlunos la = new LoginAlunos(this.facade);
                this.setVisible(false);
                la.setVisible(true);
                break;
            case 1:
                LoginDocentes ld = new LoginDocentes(this.facade);
                this.setVisible(false);
                ld.setVisible(true);
                break;
            default:
                JOptionPane.showMessageDialog(null,"Username ou Password Invalidos!");
                break;
        }
    }//GEN-LAST:event_LoginButtonActionPerformed
    
